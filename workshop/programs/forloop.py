# Program showing the use of For loops 
# Loop will first initialize the value of variable "numbers" as 0, and then its value gets incremented to become 2, 3 and so on, till it becomes 10.The loop stops when the value of numbers is 10. So, 10 is not printed. 

for numbers in range(0,10): # Try changing the values and observe the results. 
    print(numbers)           

